﻿using Serilog;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Net;
using System.Net.Http;
using System.Web;
using System.Web.Http.Filters;

namespace InterneuronFDBAPI.Infrastructure
{
    public class GlobalExceptionFilterAttribute : ExceptionFilterAttribute
    {
        public override void OnException(HttpActionExecutedContext context)
        {

            Exception ex = context.Exception;

            var errorId = Guid.NewGuid().ToString();

            if (ex.Message.StartsWith("cannot open database", StringComparison.InvariantCultureIgnoreCase) || ex.Message.StartsWith("a network", StringComparison.InvariantCultureIgnoreCase))
                Log.Logger.ForContext("ErrorId", errorId).Fatal(ex, ex.Message);
            else
                Log.Logger.ForContext("ErrorId", errorId).Error(ex, ex.Message);



            var error = $"Error executing the FDB  API. Please contact Administrator. Error Code: {errorId}";
            //context.Response.StatusCode = HttpStatusCode.InternalServerError;
            //context.Response = new HttpResponseMessage(HttpStatusCode.InternalServerError);

            var response = context.Request.CreateResponse(HttpStatusCode.InternalServerError, error, "application/json");
            context.Response = response;
        }
    }
}